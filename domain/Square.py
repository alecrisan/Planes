class Square:
    def __init__(self, x, y):
        self.x = x
        self.y = y 
        
    def get_x(self):
        """
        Gets the x coordinate of a square
        output: x
        """
        return self.x 
    
    def get_y(self):
        """
        Gets the y coordinate of a square
        output: y
        """
        return self.y